function twoRandomArrayElements (array) {
  let message = '';
  const element1 = array[createRandomIdFromRangeGenerator(0, array.length - 1)];
  const element2 = array[createRandomIdFromRangeGenerator(0, array.length - 1)];
  message = `${element1} ${element2}`;
  return message;
}


//Функция, которая генерирует и возвращает случайное целое число из диапазона
function getRandomInteger (min, max) {
  const lower = Math.ceil(Math.min(Math.abs(min), Math.abs(max)));
  const upper = Math.floor(Math.max(Math.abs(min), Math.abs(max)));
  const result = Math.random() * (upper - lower + 1) + lower;
  return Math.floor(result);
}

//Функция, которая генерирует и возвращает случайное целое число из диапазона, но и проверяет его на уникальность
function createRandomIdFromRangeGenerator (min, max) {
  const previousValues = [];
  return function () {
    let currentValue = getRandomInteger(min, max);
    if (previousValues.length >= (max - min + 1)) {
      //console.error('Перебраны все числа из диапазона от ' + min + ' до ' + max);
      return null;
    }
    while (previousValues.includes(currentValue)) {
      currentValue = getRandomInteger(min, max);
    }
    previousValues.push(currentValue);
    return currentValue;
  };
}

